// –§—É–Ω–∫—Ü–∏—è –∫–æ–Ω–≤–µ—Ä—Ç–∞—Ü–∏–∏ Markdown –≤ HTML
function markdownToHtml(markdown: string): string {
  let html = markdown;
  
  // H1 –∑–∞–≥–æ–ª–æ–≤–∫–∏
  html = html.replace(/^# (.+)$/gm, '<h1>$1</h1>');
  
  // H2 –∑–∞–≥–æ–ª–æ–≤–∫–∏
  html = html.replace(/^## (.+)$/gm, '<h2>$1</h2>');
  
  // H3 –∑–∞–≥–æ–ª–æ–≤–∫–∏
  html = html.replace(/^### (.+)$/gm, '<h3>$1</h3>');
  
  // H4 –∑–∞–≥–æ–ª–æ–≤–∫–∏
  html = html.replace(/^#### (.+)$/gm, '<h4>$1</h4>');
  
  // –ñ–∏—Ä–Ω—ã–π —Ç–µ–∫—Å—Ç
  html = html.replace(/\*\*(.+?)\*\*/g, '<strong>$1</strong>');
  
  // –ö—É—Ä—Å–∏–≤
  html = html.replace(/\*(.+?)\*/g, '<em>$1</em>');
  
  // –°–ø–∏—Å–∫–∏ (ul)
  html = html.replace(/^- (.+)$/gm, '<li>$1</li>');
  
  // –≠–º–æ–¥–∑–∏ —Å–ø–∏—Å–∫–∏
  html = html.replace(/^‚úÖ (.+)$/gm, '<p>‚úÖ $1</p>');
  html = html.replace(/^‚ö†Ô∏è (.+)$/gm, '<p>‚ö†Ô∏è $1</p>');
  html = html.replace(/^üí™ (.+)$/gm, '<p>üí™ $1</p>');
  html = html.replace(/^üéØ (.+)$/gm, '<p>üéØ $1</p>');
  html = html.replace(/^üí° (.+)$/gm, '<p>üí° $1</p>');
  html = html.replace(/^üí¨ (.+)$/gm, '<p>üí¨ $1</p>');
  html = html.replace(/^üì∏ (.+)$/gm, '<p>üì∏ $1</p>');
  
  // –ù—É–º–µ—Ä–æ–≤–∞–Ω–Ω—ã–µ —Å–ø–∏—Å–∫–∏
  html = html.replace(/^\d+\. (.+)$/gm, '<li>$1</li>');
  
  // –ü–∞—Ä–∞–≥—Ä–∞—Ñ—ã - –æ–±—Ä–∞–±–∞—Ç—ã–≤–∞–µ–º –ø–æ –±–ª–æ–∫–∞–º
  const blocks = html.split('\n\n');
  html = blocks.map(block => {
    // –ü—Ä–æ–ø—É—Å–∫–∞–µ–º –±–ª–æ–∫–∏, –∫–æ—Ç–æ—Ä—ã–µ —É–∂–µ —è–≤–ª—è—é—Ç—Å—è HTML-—Ç–µ–≥–∞–º–∏
    if (block.startsWith('<h') || block.startsWith('<ul') || block.startsWith('<ol') || 
        block.startsWith('<li') || block.startsWith('<hr') || block.startsWith('<p>')) {
      return block;
    }
    
    // –û—Ç–¥–µ–ª—å–Ω–∞—è –æ–±—Ä–∞–±–æ—Ç–∫–∞ –¥–ª—è —Å—Ç—Ä–æ–∫ —Å —ç–º–æ–¥–∑–∏ –≤ –Ω–∞—á–∞–ª–µ
    if (/^[üåüüíßü¶¥üí™üòäüìê‚ú®üßº‚è±Ô∏èüìãü™ûüíãüëÅÔ∏èüíéüõåüìöüí¨üõëüéâ]/.test(block) && !block.startsWith('<')) {
      return block;
    }
    
    const trimmed = block.trim();
    if (trimmed && !trimmed.startsWith('<')) {
      return `<p>${trimmed}</p>`;
    }
    return trimmed;
  }).join('\n\n');
  
  // –ì—Ä—É–ø–ø–∏—Ä—É–µ–º —Å–ø–∏—Å–∫–∏ –≤ <ul>
  html = html.replace(/(<li>.*?<\/li>\n?)+/g, '<ul>\n$&</ul>\n');
  
  // –ì–æ—Ä–∏–∑–æ–Ω—Ç–∞–ª—å–Ω–∞—è –ª–∏–Ω–∏—è
  html = html.replace(/^---$/gm, '<hr>');
  
  return html;
}

async function main() {
  try {
    // Connect to MongoDB
    const mongoUri = process.env.MONGODB_URI || 'mongodb://localhost:27017/rejuvena';
    console.log('üîå Connecting to MongoDB...');
    await mongoose.connect(mongoUri);
    console.log('‚úÖ Connected to MongoDB\n');

    // Update each day
    let updatedCount = 0;
    const daysToUpdate = [3, 4, 5, 6, 8, 9, 10, 12];
    
    for (const dayNumber of daysToUpdate) {
      const markdown = DAY_TEXTS[dayNumber];
      if (!markdown) {
        console.log(`‚ö†Ô∏è No text for day ${dayNumber}, skipping`);
        continue;
      }

      const html = markdownToHtml(markdown);
      
      console.log(`üìù Updating Day ${dayNumber}...`);
      console.log(`   Markdown length: ${markdown.length} chars`);
      console.log(`   HTML length: ${html.length} chars`);
      
      const day = await MarathonDay.findOneAndUpdate(
        { 
          marathonId: MARATHON_ID,
          dayNumber: dayNumber
        },
        { description: html },
        { new: true }
      );

      if (day) {
        console.log(`‚úÖ Day ${dayNumber} updated successfully`);
        updatedCount++;
      } else {
        console.log(`‚ùå Day ${dayNumber} not found`);
      }
      console.log('');
    }

    console.log(`\nüéâ Update complete! ${updatedCount}/${daysToUpdate.length} days updated.`);

    await mongoose.disconnect();
  } catch (error: any) {
    console.error('‚ùå Error:', error.message);
    process.exit(1);
  }
}

main();
