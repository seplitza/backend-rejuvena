// Convert Markdown to HTML
function markdownToHtml(markdown) {
  let html = markdown;
  
  // Headers
  html = html.replace(/### (.*?)$/gm, '<h3>$1</h3>');
  html = html.replace(/## (.*?)$/gm, '<h2>$1</h2>');
  
  // Bold
  html = html.replace(/\*\*(.*?)\*\*/g, '<strong>$1</strong>');
  
  // Italic
  html = html.replace(/\*(.*?)\*/g, '<em>$1</em>');
  
  // Lists
  html = html.replace(/^- (.*?)$/gm, '<li>$1</li>');
  html = html.replace(/(<li>.*<\/li>)/s, '<ul>$1</ul>');
  html = html.replace(/^(\d+)\. (.*?)$/gm, '<li>$2</li>');
  
  // Horizontal rule
  html = html.replace(/^---$/gm, '<hr>');
  
  // Paragraphs
  html = html.split('\n\n').map(p => {
    if (p.startsWith('<h') || p.startsWith('<ul') || p.startsWith('<hr')) {
      return p;
    }
    return `<p>${p}</p>`;
  }).join('\n');
  
  return html;
}

async function updateDays() {
  try {
    console.log('üîå Connecting to MongoDB...');
    await mongoose.connect(MONGO_URI, {
      dbName: DB_NAME,
      useNewUrlParser: true,
      useUnifiedTopology: true
    });
    console.log('‚úÖ Connected to MongoDB');

    const db = mongoose.connection.db;
    const collection = db.collection('marathondays');

    // Update Days 3-7
    for (let dayNum = 3; dayNum <= 7; dayNum++) {
      const dayId = DAY_IDS[dayNum];
      const markdownText = DAY_TEXTS[dayNum];
      const htmlText = markdownToHtml(markdownText);

      console.log(`\nüìù Updating Day ${dayNum} (ID: ${dayId})...`);
      console.log(`   Markdown length: ${markdownText.length} chars`);
      console.log(`   HTML length: ${htmlText.length} chars`);

      const result = await collection.updateOne(
        { _id: new mongoose.Types.ObjectId(dayId) },
        { $set: { description: htmlText } }
      );

      if (result.modifiedCount > 0) {
        console.log(`‚úÖ Day ${dayNum} updated successfully!`);
      } else {
        console.log(`‚ö†Ô∏è  Day ${dayNum} not modified (maybe already up to date?)`);
      }
    }

    console.log('\nüéâ All days processed!');
    
  } catch (error) {
    console.error('‚ùå Error:', error);
  } finally {
    await mongoose.connection.close();
    console.log('‚úÖ Connection closed');
  }
}

